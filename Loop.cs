namespace MyConsoleApp
{
    public class Loop
    {
        public static void LoopMain()
        {


            // For Loop
            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine(i);
            }

            // While Loop
            int j = 0;
            while (j < 10)
            {
                Console.WriteLine(j);
                j++;
            }
        }
    }
}